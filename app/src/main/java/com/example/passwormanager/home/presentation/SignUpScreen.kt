@Composable
fun SignUpScreen(
    authViewModel: AuthViewModel,
    onNavigateToLogin: () -> Unit
) {
    var email by remember { mutableStateOf("") }
    var password by remember { mutableStateOf("") }
    var firstName by remember { mutableStateOf("") }
    var lastName by remember { mutableStateOf("") }
    var passwordVisible by remember { mutableStateOf(false) }


    var isInvalidEmail by remember {
        authViewModel.isInvalidEmail
    }
    var isInvalidPassword by remember {
        authViewModel.isInvalidPassword
    }

    if(isInvalidEmail)  {
        Toast.makeText(LocalContext.current, "Email not valid", Toast.LENGTH_SHORT).show()
        authViewModel.isInvalidEmail.value=false
    }

    if(isInvalidPassword)  {
        Toast.makeText(LocalContext.current, "Password not valid", Toast.LENGTH_SHORT).show()
        authViewModel.isInvalidPassword.value=false
    }

    BackHandler {
        onNavigateToLogin()
    }

    Scaffold { it ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(16.dp),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            OutlinedTextField(
                value = email,
                onValueChange = { email = it },
                label = { Text("Email") },
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp)
            )
            OutlinedTextField(
                value = password,
                onValueChange = { password = it },
                label = { Text("Password") },
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp),
                visualTransformation = if (passwordVisible) VisualTransformation.None else PasswordVisualTransformation(),
                trailingIcon = {
                    val image = if (passwordVisible)
                        Icons.Filled.Visibility
                    else
                        Icons.Filled.VisibilityOff

                    IconButton(onClick = {
                        passwordVisible = !passwordVisible
                    }) {
                        Icon(imageVector = image, contentDescription = null)
                    }
                }
            )
            OutlinedTextField(
                value = firstName,
                onValueChange = { firstName = it },
                label = { Text("First Name") },
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp)
            )
            OutlinedTextField(
                value = lastName,
                onValueChange = { lastName = it },
                label = { Text("Last Name") },
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp)
            )
            Button(
                onClick = {
                    authViewModel.signUp(email, password, firstName, lastName)
                    email = ""
                    password = ""
                    firstName = ""
                    lastName = ""
                },
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(8.dp)
            ) {
                Text("Sign Up")
            }
            Spacer(modifier = Modifier.height(16.dp))
            Text("Already have an account? Sign in.",
                modifier = Modifier.clickable {
                    onNavigateToLogin()
                }
            )
        }
    }
}


